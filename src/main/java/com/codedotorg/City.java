package com.codedotorg;

public class City {
    
    /** The name of a City */
    private String name;

    /**
     * Constructor for the City class.
     *
     * @param name The name of the city.
     */
    public City(String name) {
        this.name = name;
    }

    /**
     * Gets the name of the city.
     * @return String The name of the city.
     */
    public String getName() {
        return name;
    }

}
